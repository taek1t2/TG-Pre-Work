let athleteFinalPosition = 'first place';
switch(athleteFinalPosition) {
  case 'first place':
    	console.log('You get the gold medal!');
    break;
  case 'second place':
    	console.log('You get the silver medal!');
    break;
  case 'third place':
    	console.log('You get the bronze medal!');
    break;
  default:
    console.log('No medal awarded.');
    break;
}




//A switch statement provides an alternative syntax that is easier to read and write.
//case keyword checks if the expression matches the specified value that comes after it.
//break keyword will exit the block and not execute any more code or check any other cases inside the code block.
